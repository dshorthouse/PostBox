        <dt><a name="q2"></a>What is a Darwin Core Archive?</dt>
        <dd><a href="http://www.gbif.org">The Global Biodiversity Information Facility</a> (GBIF) developed a data exchange format for publishing annotated checklist data based on the ratified Darwin Core terms. A Darwin Core Archive (DwC-A) consists of a set of files where one file represents the 'core' taxonomic data. A single row in this file represents a single taxon reference. Other files represent "extensions" to this core table and allow additional data elements to be linked to a taxon with a many to one relationship. A special file, called meta.xml is used as a map to describe the core taxon file and any extensions. Each field or column is identified and described so that the DwC-A package can be interpreted. An additional file called eml.xml describes the dataset as a whole. This file includes information about the entire checklist such as the title, authors, abstract, etc. (adapted from <a href="http://code.google.com/p/gbif-ecat/wiki/DwCArchive">GBIF-ECAT</a>) <img src="../images/dwca.png" alt="Darwin Core Archive structure"></dd>

